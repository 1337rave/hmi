# Запит двох чисел від користувача
число1 = float(input("Введіть перше число: "))
число2 = float(input("Введіть друге число: "))

# Перевірка на рівність чисел
if число1 == число2:
    print("Числа рівні.")
else:
    # Визначення мінімального та максимального числа
    мінімальне = min(число1, число2)
    максимальне = max(число1, число2)

    # Виведення чисел у порядку зростання
    print(f"Числа у порядку зростання: {мінімальне}, {максимальне}")
